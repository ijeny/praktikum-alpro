print('OPERATOR AND')
print('semua statement benar :',
      (6>4) and (8>5))
print('salah satu statement salah :',
      (6>4) and (8<3))
print('-------------')

print('OPERATOR OR')
print('semua statement salah :',
      (8<4) or (9<6))
print('salah satu statement benar :',
      (9>7) or (6<8))
print('-------------')

print('OPERATOR NOT')
print('statement benar :', not(9>7)) 
print('statement salah :', not(8<4))
print('-------------')