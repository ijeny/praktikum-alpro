# buat 2 variabel
a = 4
b = 6

# penjumlahan(+)
penjumlahan = a+b
print('hasil penjumlahan :', penjumlahan)

# pengurangan(-)
pengurangan = a-b
print('hasil pengurangan :', pengurangan)

# perkalian(*)
perkalian = a*b
print('hasil perkalian :', perkalian)

# pangkat(**)
pangkat = a**b
print('hasil pangkat :', pangkat)

# pembagian (/)
pembagian = a/b
print('hasil pembagian :', pembagian)

# pembagian kebawah(//)
PembagianKebawah = a//b
print('hasil pembagian kebawah :', PembagianKebawah)

# sisa bagi(%)
SisaBagi = a%b
print('hasil sisa bagi :', SisaBagi)

# precedence
precedence = 1 + 4 * 5 - 2
print('hasil precedence :', precedence)

# precedence
Precedence = (1 + 4) * 5 - 2
print('hasil Precedence :', Precedence)