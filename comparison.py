# operator >
print('apakah benar 6>9?', 6>9)
print('apakah benar 9>5?', 9>5)

# operator <
print('apakah benar 3<4?', 3<4)
print('apakah benar 5<5?', 5<5)

# operator >=
print('apakah benar 6>=4?', 6>=4)
print('apakah benar 8>=8?', 8>=8)

# operator <=
print('apakah benar 10<=15?', 10<=15)
print('apakah benar 5<=5?', 5<=5)

# operator ==
print('apakah benar 6==6?', 6==6)
print('apakah benar 4==6?', 4==6)

# operator !=
print('apakah benar 6!=3?', 6!=3)
print('apakah benar 4!=4?', 4!=4)