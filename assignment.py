# operator
a = 9
b = 12

# operator +=
a += 4
# a = a + 4
print(a)

# operator -=
b -= 5
# b = b - 5
print(b)

# operator *=
b *= 4
# b = b * 4
print(b)

# operator **=
a **= 2
# a = a ** 2
print(a)

# operator /=
b /= 3
# b = b / 3
print(b)

# operator //=
a //= 2
# a = a // 2
print(a)

# operator %=
b %= 5
# b = b % 5
print(b)